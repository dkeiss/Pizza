-- User
CREATE TABLE USER
(
  ID                INTEGER      NOT NULL AUTO_INCREMENT,
  USERNAME          VARCHAR(255) NOT NULL UNIQUE,
  PASSWORD          VARCHAR(255),
  PASSWORD_TYPE     VARCHAR(255),
  ADMIN             BOOLEAN      NOT NULL,
  FIRSTNAME         VARCHAR(255),
  LASTNAME          VARCHAR(255),
  DISCOUNT          DECIMAL(10, 2),
  CREATION_DATE     DATE,
  MODIFICATION_DATE DATE,
  PRIMARY KEY (ID)
);
CREATE SEQUENCE USER_ID_SEQ;

-- Product
CREATE TABLE PRODUCT
(
  ID            INTEGER      NOT NULL AUTO_INCREMENT,
  NAME          VARCHAR(255) NOT NULL,
  TYPE          VARCHAR(255) NOT NULL,
  CREATION_DATE DATE,
  PRIMARY KEY (ID)
);
CREATE SEQUENCE PRODUCT_ID_SEQ;

-- Productvariant
CREATE TABLE PRODUCT_PRODUCTVARIANT (
  ID                INTEGER NOT NULL AUTO_INCREMENT,
  PRODUCT_ID        INTEGER NOT NULL,
  PRODUCTVARIANT_ID INTEGER NOT NULL,
  CREATION_DATE     DATE
);

CREATE TABLE PRODUCTVARIANT
(
  ID            INTEGER      NOT NULL AUTO_INCREMENT,
  NAME          VARCHAR(255) NOT NULL,
  CREATION_DATE DATE
);

-- Productgroup
CREATE TABLE PRODUCTGROUP
(
  ID            INTEGER      NOT NULL AUTO_INCREMENT,
  NAME          VARCHAR(255) NOT NULL,
  CREATION_DATE DATE
);

-- Product ingredient
CREATE TABLE PRODUCT_INGREDIENT
(
  ID            INTEGER      NOT NULL AUTO_INCREMENT,
  NAME          VARCHAR(255) NOT NULL,
  CREATION_DATE DATE
);

-- Additional category
CREATE TABLE ADDITIONAL_CATEGORY
(
  ID            INTEGER      NOT NULL AUTO_INCREMENT,
  NAME          VARCHAR(255) NOT NULL,
  CREATION_DATE DATE
);
CREATE SEQUENCE ADDITIONAL_CATEGORY_ID_SEQ;

-- Ingredients price
CREATE TABLE INGREDIENTS_PRICE
(
  ID            INTEGER      NOT NULL AUTO_INCREMENT,
  NAME          VARCHAR(255) NOT NULL,
  CREATION_DATE DATE
);
CREATE SEQUENCE INGREDIENTS_PRICE_ID_SEQ;
